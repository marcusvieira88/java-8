package com.github.marcusvieira.integer;

public class IntegerPratice {

    public static void main(String[] args) {
        System.out.println("---- Integer max ------");
        System.out.println(Integer.max(123,675));
        System.out.println("---- Integer min ------");
        System.out.println(Integer.min(123,675));
        System.out.println("---- Integer compare ------");
        System.out.println(Integer.compare(123,675));
        System.out.println("---- Integer sum ------");
        System.out.println(Integer.sum(123,675));
        System.out.println("---- Integer divideUnsigned ------");
        System.out.println(Integer.divideUnsigned(25,5));
    }
}
